

def make_prompt(input_text):
    prompt = f"""
    あなたは高度な技術を持つチームリーダーです。 以下の指示と上司からのレビューコメントに基づき、中級者の部下が技術的背景を深く理解できるような、プロフェッショナルなレビューコメントを作成してください。

    {input_text}

    回答の条件:

    専門用語は適切に使用し、その概念も簡潔に説明すること。

    なぜその変更がベストプラクティスなのか、技術的な観点から説明すること。

    コードの品質向上やメンテナンス性など、変更のメリットを明確に伝えること。

    回答を箇条書きで構成し、各項目の要点を太字で強調すること。

    思考プロセス:

    上司のコメントの技術的な意図を深く理解する。

    各指摘に関連する技術的な概念（例：デザインパターン、アーキテクチャ）を特定する。

    その概念と変更の必要性を結びつけて説明する。

    上記を中級者に向けた形式に整える。
    """

    return prompt
